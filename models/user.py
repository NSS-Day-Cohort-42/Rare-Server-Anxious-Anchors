class User():
    def __init__(self, id, email, password, firstName, lastName, displayName, photo, profileCreation, profileType):
        self.id = id
        self.email = email
        self.password = password
        self.firstName = firstName
        self.lastName = lastName
        self.displayName = displayName
        self.photo = photo
        self.profileCreation = profileCreation
        self.profileType = profileType